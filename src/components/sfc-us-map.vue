			this.$emit('click', state)
		},

		activeState(state){
			return state === this.currentStateId
		},

		stateHover(state) {
			if(this.$refs[state].classList){
				this.$refs[state].classList.add('hovered')
			}else{
				let classList = this.$refs[state].getAttribute("class").split(" ")
				classList.push("hovered")
				classList = classList.join(" ")
				this.$refs[state].setAttribute("class", classList)
			}
		},
		stateHoverOff(state) {
			this.$emit('mouseended', state)
			if (state != this.currentStateId){
				if(this.$refs[state].classList){
					this.$refs[state].classList.remove('hovered')
				}else{
					let classList = this.$refs[state].getAttribute("class").split(" ")

					for (let index = 0; index < classList.length; index++) {
						if(classList[index] == "hovered"){
							classList[index] = ""
						}
					}
					classList = classList.join(" ")
					this.$refs[state].setAttribute("class", classList)
				}
			}
		},
	},

	created(){
	},

	data(){
		let ready = true

		return {
			mapWidth: 0,
			ready
		}
	}
}
