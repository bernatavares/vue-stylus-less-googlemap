<script>
export default {

	props: {
		selectedStateId: {
			type: String,
			default: ""
		},
		colorMain: {
			type: String,
			default: "95A1AC"
		},
		colorStroke: {
			type: String,
			default: "ffffff"
		}
	},

	watch: {
		selectedStateId: function(newVal, oldVal) {
			this.currentStateId = newVal
			if (oldVal && oldVal != newVal){
				if(this.$refs[oldVal].classList){
					this.$refs[oldVal].classList.remove('hovered')
				}else{
					let classList = this.$refs[oldVal].getAttribute("class").split(" ")

					for (let index = 0; index < classList.length; index++) {
						if(classList[index] == "hovered"){
							classList[index] = ""
						}
					}
					classList = classList.join(" ")
					this.$refs[oldVal].setAttribute("class", classList)
				}
			}

			if(this.$refs[this.currentStateId].classList){
				this.$refs[this.currentStateId].classList.add('hovered')
			}else{
				let classList = this.$refs[this.currentStateId].getAttribute("class").split(" ")
				classList.push("hovered")
				classList = classList.join(" ")
				this.$refs[this.currentStateId].setAttribute("class", classList)
			}
		}
	},

	computed: {
		height(){
			return "100%"
		},
		customColorMain(){
